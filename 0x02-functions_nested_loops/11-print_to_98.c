#include <stdio.h>
#include "main.h"
/**
 * print_to_98 - entry point
 *
 * Description: return countdown
 *
 * @n: is the expected parameter
 *
 * Return: loop an integer to return 98
 */
void print_to_98(int n)
{
	if (n < 98)
	{
		while (n <= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n++;
		}
	}
	else if (n > 98)
	{
		while (n >= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n--;
		}
	}
	else
	{
		printf("98");
	}
	printf("\n");
}
